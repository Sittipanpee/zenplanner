/**
 * Moon icon — crescent shape.
 * Inline SVG to avoid external icon library dependency.
 */
function MoonIcon() {
  return (
    <svg
      width="18"
      height="18"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z" />
    </svg>
  )
}

/**
 * Theme toggle button.
 * Uses next-themes for persistence and system preference detection.
 * Renders a placeholder during SSR to prevent hydration mismatch flash.
 */
export default function ThemeToggle() {
  const { theme, setTheme } = useTheme()
  const [mounted, setMounted] = useState(false)

  useEffect(() => setMounted(true), [])

  /* Prevent hydration flash: render an inert placeholder matching the button size */
  if (!mounted) {
    return <div className="zen-btn-icon" style={{ visibility: 'hidden' }} />
  }

  const isDark = theme === 'dark'

  return (
    <button
      type="button"
      onClick={() => setTheme(isDark ? 'light' : 'dark')}
      className="zen-btn-icon"
      aria-label={isDark ? 'Switch to light mode' : 'Switch to dark mode'}
    >
      {isDark ? <SunIcon /> : <MoonIcon />}
    </button>
  )
}
